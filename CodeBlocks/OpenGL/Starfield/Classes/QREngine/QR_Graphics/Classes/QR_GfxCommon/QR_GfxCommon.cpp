#include "QR_GfxCommon.h"
#include <memory>

#ifdef __WIN32__
    // needed for std::memset, otherwise compiler will not found it
    #include <cstring>
#endif

//------------------------------------------------------------------------------
// QR_Point
//------------------------------------------------------------------------------
QR_Point::QR_Point()
{
    std::memset(this, 0, sizeof(*this));
}
//------------------------------------------------------------------------------
QR_Point::QR_Point(int x, int y) : m_X(x), m_Y(y)
{}
//------------------------------------------------------------------------------
// QR_Size
//------------------------------------------------------------------------------
QR_Size::QR_Size()
{
    std::memset(this, 0, sizeof(*this));
}
//------------------------------------------------------------------------------
QR_Size::QR_Size(unsigned width, unsigned height) : m_Width(width), m_Height(height)
{}
//------------------------------------------------------------------------------
// QR_Screen
//------------------------------------------------------------------------------
QR_Screen::QR_Screen()
{
    std::memset(this, 0, sizeof(*this));
}
//------------------------------------------------------------------------------
QR_Screen::QR_Screen(unsigned width, unsigned height) : m_Width(width), m_Height(height)
{}
