#include "QR_Diamond.h"

//------------------------------------------------------------------------------
QR_Diamond::QR_Diamond() : QR_Piece()
{
    m_Right  = 4;
    m_Bottom = 1;
    m_Color  = QR_Cell::IE_Gray;
    m_Name   = L"Diamond";

    // create buffer indicating how piece should be filled
    char fillArray[] = {1 , 1 , 1 , 1};

    // create piece
    Create(fillArray, 4);
}
