#include "QR_Cell.h"

//------------------------------------------------------------------------------
// class QR_Cell - c++
//------------------------------------------------------------------------------
QR_Cell::QR_Cell() :
    m_Color(IE_White),
    m_Status(IE_Empty)
{}
//------------------------------------------------------------------------------
QR_Cell::~QR_Cell()
{}
//------------------------------------------------------------------------------
void QR_Cell::Clear()
{
    m_Color  = IE_White;
    m_Status = IE_Empty;
}
//------------------------------------------------------------------------------
QR_Cell::IEColor QR_Cell::GetColor()
{
    return m_Color;
}
//------------------------------------------------------------------------------
void QR_Cell::SetColor(IEColor color)
{
    m_Color = color;
}
//------------------------------------------------------------------------------
QR_Cell::IEStatus QR_Cell::GetStatus()
{
    return m_Status;
}
//------------------------------------------------------------------------------
void QR_Cell::SetStatus(IEStatus status)
{
    m_Status = status;
}
