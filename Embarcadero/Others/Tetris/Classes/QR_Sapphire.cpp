#include "QR_Sapphire.h"

//------------------------------------------------------------------------------
QR_Sapphire::QR_Sapphire() : QR_Piece()
{
    m_Bottom = 2;
    m_Right  = 3;
    m_Color  = QR_Cell::IE_LightBlue;
    m_Name   = L"Sapphire";

    // create buffer indicating how piece should be filled
    char fillArray[] = {0, 1, 0,
                        1, 1, 1};

    // create piece
    Create(fillArray, 6);
}
